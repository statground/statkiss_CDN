let gv_active_users_table_cache = null

async function get_active_users_table_cache() {
    if (gv_active_users_table_cache == null) {
        gv_active_users_table_cache = await fetch('/admin/ajax_get_active_user_table/')
            .then(res => res.json())
            .then(res => res)
    }
    return gv_active_users_table_cache
}

async function render_active_users_counter() {
    const data = await fetch('/admin/ajax_get_active_user_counter/')
        .then(res => res.json())
        .then(res => res)

    function Div_counter(props) {
        return (
            <div class="flex flex-col justify-center items-center w-full space-y-4">
                <div class="flex flex-col justify-center items-center w-full border border-blue-200 shadow p-8 space-y-4 bg-white rounded-lg">
                    <h1 class="mb-2 text-4xl font-extrabold text-gray-900 md:text-5xl text-center">
                        <span class="text-transparent bg-clip-text bg-gradient-to-r to-emerald-600 from-sky-400">
                            {admin_t('active_users.title')}
                        </span>
                    </h1>
                    <p class="text-center text-gray-600 max-w-3xl">
                        {admin_t('active_users.description')}
                    </p>

                    <div class="grid grid-cols-4 md:grid-cols-2 gap-4 w-full">
                        <Div_card title={admin_t('admin.common.total')} count={props.data.cnt_total} />
                        <Div_card title={admin_t('admin.common.current_year')} count={props.data.cnt_yearly} />
                        <Div_card title={admin_t('admin.common.current_month')} count={props.data.cnt_monthly} />
                        <Div_card title={admin_t('admin.common.today')} count={props.data.cnt_daily} />
                    </div>
                </div>
            </div>
        )
    }

    ReactDOM.render(<Div_counter data={data} />, document.getElementById('div_counter'))
}

async function render_active_users_graph(type) {
    const divGraphDaily = document.getElementById('graph_daily')
    const divGraphMonthly = document.getElementById('graph_monthly')
    const divGraphYearly = document.getElementById('graph_yearly')
    const classDivGraph = 'p-4 rounded-lg bg-gray-50 w-full'

    const options = {
        series: {
            0: { targetAxisIndex: 0 },
            1: { targetAxisIndex: 1 }
        },
        width: '100%',
        legend: { position: 'none' },
        crosshair: { orientation: 'vertical', trigger: 'focus' },
        focusTarget: 'category',
        hAxis: { textStyle: { color: 'green' }, format: 'yy-MM-dd' },
        explorer: { actions: ['dragToZoom', 'rightClickToReset'], axis: 'horizontal' },
        bar: { groupWidth: '90%' }
    }

    const data = await get_active_users_table_cache()

    if (type == 'daily') {
        divGraphDaily.className = classDivGraph
        divGraphMonthly.className = 'hidden'
        divGraphYearly.className = 'hidden'
        ReactDOM.render(<Div_graph_skeleton />, document.getElementById('graph_daily'))
        options['title'] = admin_t('active_users.graph.daily')
        bar(data.daily, 'graph_daily', options)
    } else if (type == 'monthly') {
        divGraphDaily.className = 'hidden'
        divGraphMonthly.className = classDivGraph
        divGraphYearly.className = 'hidden'
        ReactDOM.render(<Div_graph_skeleton />, document.getElementById('graph_monthly'))
        options['title'] = admin_t('active_users.graph.monthly')
        bar(data.monthly, 'graph_monthly', options)
    } else {
        divGraphDaily.className = 'hidden'
        divGraphMonthly.className = 'hidden'
        divGraphYearly.className = classDivGraph
        ReactDOM.render(<Div_graph_skeleton />, document.getElementById('graph_yearly'))
        options['title'] = admin_t('active_users.graph.yearly')
        bar(data.yearly, 'graph_yearly', options)
    }
}

function set_content() {
    function Div_content() {
        return (
            <div class="flex flex-col w-full space-y-8">
                <div id="div_counter" class="w-full"></div>
                <div id="div_graph" class="w-full bg-white border border-gray-200 rounded-lg shadow p-6">
                    <div class="flex flex-row justify-start items-center space-x-8 py-2 mb-4 border-b border-gray-900">
                        <span class="hover:underline cursor-pointer" onClick={() => render_active_users_graph('daily')}>{admin_t('admin.common.daily')}</span>
                        <span class="hover:underline cursor-pointer" onClick={() => render_active_users_graph('monthly')}>{admin_t('admin.common.monthly')}</span>
                        <span class="hover:underline cursor-pointer" onClick={() => render_active_users_graph('yearly')}>{admin_t('admin.common.yearly')}</span>
                    </div>
                    <div class="hidden" id="graph_daily"></div>
                    <div class="w-full" id="graph_monthly"></div>
                    <div class="hidden" id="graph_yearly"></div>
                </div>
            </div>
        )
    }

    gv_active_users_table_cache = null
    ReactDOM.render(<Div_content />, document.getElementById('div_content'))
    render_active_users_counter()
    render_active_users_graph('monthly')
}

function set_main() {
    render_admin_shell('active-users', set_content)
}
