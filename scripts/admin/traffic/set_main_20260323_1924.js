let gv_traffic_table_cache = {
    pageview: null,
    visitor: null
}

async function get_traffic_counter(metric) {
    const endpointMap = {
        pageview: '/admin/ajax_get_pageview_counter/',
        visitor: '/admin/ajax_get_visitor_counter/'
    }

    return await fetch(endpointMap[metric])
        .then(res => res.json())
        .then(res => res)
}

async function get_traffic_table(metric) {
    if (gv_traffic_table_cache[metric] == null) {
        const endpointMap = {
            pageview: '/admin/ajax_get_pageview_table/',
            visitor: '/admin/ajax_get_visitor_table/'
        }
        gv_traffic_table_cache[metric] = await fetch(endpointMap[metric])
            .then(res => res.json())
            .then(res => res)
    }
    return gv_traffic_table_cache[metric]
}

function traffic_graph_title(metric, type) {
    if (metric == 'pageview') {
        if (type == 'daily') return admin_t('traffic.pageview.graph.daily')
        if (type == 'monthly') return admin_t('traffic.pageview.graph.monthly')
        return admin_t('traffic.pageview.graph.yearly')
    }

    if (type == 'daily') return admin_t('traffic.visitor.graph.daily')
    if (type == 'monthly') return admin_t('traffic.visitor.graph.monthly')
    return admin_t('traffic.visitor.graph.yearly')
}

async function render_traffic_graph(metric, type) {
    const divGraphDaily = document.getElementById(metric + '_graph_daily')
    const divGraphMonthly = document.getElementById(metric + '_graph_monthly')
    const divGraphYearly = document.getElementById(metric + '_graph_yearly')
    const classDivGraph = 'p-4 rounded-lg bg-gray-50 w-full'

    const options = {
        series: {
            0: { targetAxisIndex: 0 },
            1: { targetAxisIndex: 1 }
        },
        width: '100%',
        legend: { position: 'none' },
        crosshair: { orientation: 'vertical', trigger: 'focus' },
        focusTarget: 'category',
        hAxis: { textStyle: { color: 'green' }, format: 'yy-MM-dd' },
        explorer: { actions: ['dragToZoom', 'rightClickToReset'], axis: 'horizontal' },
        bar: { groupWidth: '90%' }
    }

    const data = await get_traffic_table(metric)

    if (type == 'daily') {
        divGraphDaily.className = classDivGraph
        divGraphMonthly.className = 'hidden'
        divGraphYearly.className = 'hidden'
        ReactDOM.render(<Div_graph_skeleton />, document.getElementById(metric + '_graph_daily'))
        options['title'] = traffic_graph_title(metric, 'daily')
        bar(data.daily, metric + '_graph_daily', options)
    } else if (type == 'monthly') {
        divGraphDaily.className = 'hidden'
        divGraphMonthly.className = classDivGraph
        divGraphYearly.className = 'hidden'
        ReactDOM.render(<Div_graph_skeleton />, document.getElementById(metric + '_graph_monthly'))
        options['title'] = traffic_graph_title(metric, 'monthly')
        bar(data.monthly, metric + '_graph_monthly', options)
    } else {
        divGraphDaily.className = 'hidden'
        divGraphMonthly.className = 'hidden'
        divGraphYearly.className = classDivGraph
        ReactDOM.render(<Div_graph_skeleton />, document.getElementById(metric + '_graph_yearly'))
        options['title'] = traffic_graph_title(metric, 'yearly')
        bar(data.yearly, metric + '_graph_yearly', options)
    }
}

