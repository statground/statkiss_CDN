    function resolveLangCode(code) {
        if (window.StatKISS_I18N && typeof window.StatKISS_I18N.resolveLangCode === 'function') {
            return window.StatKISS_I18N.resolveLangCode(code || 'en');
        }

        if (!code) return 'en';
        const c = String(code).trim();
        const lower = c.toLowerCase();

        if (c === 'zh' || lower.startsWith('zh-')) {
            if (lower.includes('tw') || lower.includes('hk') || lower.includes('mo') || lower.includes('hant')) return 'zh-Hant';
            return 'zh-Hans';
        }
        if (c === 'tl' || lower.startsWith('tl-') || c === 'fil' || lower.startsWith('fil-')) return 'fil';
        if (c === 'pt' || lower.startsWith('pt-')) return 'pt-BR';
        if (lower.startsWith('en-')) return 'en';
        if (lower.startsWith('ja-')) return 'ja';
        if (lower.startsWith('ko-')) return 'ko';
        return c;
    }

    function getSupportedLangs() {
        if (window.StatKISS_I18N && Array.isArray(window.StatKISS_I18N.languages)) {
            return window.StatKISS_I18N.languages.map(function (item) { return item.code; });
        }
        return DEFAULT_LANGS.slice();
    }

    function normalizeCoverage() {
        const en = messages.en || {};
        const ko = messages.ko || {};
        getSupportedLangs().forEach(function (lang) {
            if (!messages[lang]) messages[lang] = {};
            UI_KEYS.forEach(function (key) {
                if (messages[lang][key] == null || messages[lang][key] === '') {
                    messages[lang][key] = en[key] ?? ko[key] ?? key;
                }
            });
        });
    }

    function getCurrentLang() {
        const supported = getSupportedLangs();

        const parts = window.location.pathname.split('/').filter(Boolean);
        if (parts.length) {
            const pathLang = resolveLangCode(parts[0]);
            if (supported.includes(pathLang)) return pathLang;
        }

        const htmlLang = document.documentElement.getAttribute('lang');
        if (htmlLang) {
            const normalizedHtmlLang = resolveLangCode(htmlLang);
            if (supported.includes(normalizedHtmlLang)) return normalizedHtmlLang;
        }

        try {
            const langKey = window.StatKISS_I18N && window.StatKISS_I18N.LANG_KEY ? window.StatKISS_I18N.LANG_KEY : 'statkiss_lang';
            const saved = localStorage.getItem(langKey);
            if (saved) {
                const savedLang = resolveLangCode(saved);
                if (supported.includes(savedLang)) return savedLang;
            }
        } catch (_) {}

        return resolveLangCode(navigator.language || 'en');
    }

    function t(key, lang) {
        const code = resolveLangCode(lang || getCurrentLang());
        const current = messages[code] || {};
        const en = messages.en || {};
        const ko = messages.ko || {};
        return current[key] ?? en[key] ?? ko[key] ?? key;
    }

