function mergeIntoDonationsI18n() {
  const api = window.StatKISS_DONATIONS_I18N;
  if (!api || !api.dict) return;

  if (!Array.isArray(api.keys)) api.keys = [];
  EXTRA_KEYS.forEach((key) => {
    if (!api.keys.includes(key)) api.keys.push(key);
  });

  Object.keys(EXTRA_DICT).forEach((lang) => {
    if (!api.dict[lang]) api.dict[lang] = {};
    Object.assign(api.dict[lang], EXTRA_DICT[lang]);
  });

  if (typeof api.normalizeCoverage === 'function') {
    api.normalizeCoverage();
  }
}

mergeIntoDonationsI18n();
