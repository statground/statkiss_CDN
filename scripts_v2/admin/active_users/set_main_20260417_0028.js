function Div_kpi_card(props) {
    const accent = props.accent || 'blue';
    const accentClass = accent === 'amber'
        ? 'from-amber-500/15 to-orange-500/5 dark:from-amber-400/15 dark:to-orange-400/5'
        : (accent === 'emerald'
            ? 'from-emerald-500/15 to-teal-500/5 dark:from-emerald-400/15 dark:to-teal-400/5'
            : 'from-blue-500/15 to-sky-500/5 dark:from-blue-400/15 dark:to-sky-400/5');

    return (
        <div class={"rounded-2xl border border-slate-200 bg-gradient-to-br " + accentClass + " p-6 shadow-sm dark:border-slate-700 dark:bg-slate-900/80"}>
            <div class="text-4xl font-extrabold tracking-tight text-slate-900 dark:text-white">
                {au_format_number(props.value || 0)}
            </div>
            <div class="mt-2 text-lg font-semibold text-slate-900 dark:text-slate-100">{props.title}</div>
            <div class="mt-2 text-sm leading-6 text-slate-500 dark:text-slate-300">
                {props.children}
            </div>
        </div>
    );
}

function Div_active_users_dashboard(props) {
    const data = props.data || {};

    const rowsByTab = React.useMemo(function() {
        return {
            daily: au_sorted_rows(data.list_daily),
            monthly: au_sorted_rows(data.list_monthly),
            yearly: au_sorted_rows(data.list_yearly),
            total: au_sorted_rows(data.list_total),
        };
    }, [data]);

    const defaultDates = React.useMemo(function() {
        return {
            daily: au_last_date(rowsByTab.daily),
            monthly: au_last_date(rowsByTab.monthly),
            yearly: au_last_date(rowsByTab.yearly),
        };
    }, [rowsByTab]);

    const [activeTab, setActiveTab] = React.useState('monthly');
    const [selectedDates, setSelectedDates] = React.useState(defaultDates);

    React.useEffect(function() {
        setSelectedDates(function(prev) {
            return {
                daily: prev && prev.daily ? prev.daily : defaultDates.daily,
                monthly: prev && prev.monthly ? prev.monthly : defaultDates.monthly,
                yearly: prev && prev.yearly ? prev.yearly : defaultDates.yearly,
            };
        });
    }, [defaultDates.daily, defaultDates.monthly, defaultDates.yearly]);

    const activeRows = React.useMemo(function() {
        return au_filter_rows(activeTab, rowsByTab, selectedDates);
    }, [activeTab, rowsByTab, selectedDates]);

    const snapshot = React.useMemo(function() {
        return au_compute_snapshot(activeTab, activeRows, data.summary_total || {});
    }, [activeTab, activeRows, data.summary_total]);

    React.useEffect(function() {
        if (!activeRows.length && activeTab !== 'total') return;
        au_render_charts(activeRows, snapshot, activeTab);
    }, [activeRows, snapshot, activeTab]);

    const hasAnyData = !!(
        rowsByTab.daily.length
        || rowsByTab.monthly.length
        || rowsByTab.yearly.length
        || rowsByTab.total.length
        || au_safe_number(data && data.summary_total && data.summary_total.cnt_visit)
        || au_safe_number(data && data.summary_total && data.summary_total.cnt_signup)
        || au_safe_number(data && data.summary_total && data.summary_total.cnt_login)
    );

    const tabButtonClass = function(tabKey) {
        const isActive = activeTab === tabKey;
        return 'inline-flex items-center rounded-full border px-4 py-2 text-sm font-semibold transition ' + (
            isActive
                ? 'border-blue-600 bg-blue-600 text-white shadow-sm dark:border-blue-400 dark:bg-blue-500 dark:text-white'
                : 'border-slate-300 bg-white text-slate-700 hover:border-slate-400 hover:bg-slate-50 dark:border-slate-700 dark:bg-slate-900 dark:text-slate-200 dark:hover:border-slate-500 dark:hover:bg-slate-800'
        );
    };

    const pickerValue = activeTab === 'daily'
        ? (selectedDates.daily || defaultDates.daily)
        : (activeTab === 'monthly'
            ? (selectedDates.monthly || defaultDates.monthly)
            : (selectedDates.yearly || defaultDates.yearly));

    const pickerMin = activeTab === 'daily'
        ? au_first_date(rowsByTab.daily)
        : (activeTab === 'monthly'
            ? au_first_date(rowsByTab.monthly)
            : au_first_date(rowsByTab.yearly));

    const pickerMax = activeTab === 'daily'
        ? au_last_date(rowsByTab.daily)
        : (activeTab === 'monthly'
            ? au_last_date(rowsByTab.monthly)
            : au_last_date(rowsByTab.yearly));

    const onChangePicker = function(e) {
        const value = e && e.target ? e.target.value : '';
        setSelectedDates(function(prev) {
            const next = Object.assign({}, prev || {});
            next[activeTab] = value;
            return next;
        });
    };

    return (
        <div class="w-full max-w-screen-2xl space-y-6">
            <div class="rounded-2xl border border-slate-200 bg-white p-6 shadow-sm dark:border-slate-700 dark:bg-slate-900">
                <div class="flex flex-col gap-4 lg:flex-row lg:items-start lg:justify-between">
                    <div>
                        <h1 class="text-3xl font-extrabold tracking-tight text-slate-900 dark:text-white">{admin_t('active_users.title')}</h1>
                        <p class="mt-2 max-w-3xl text-sm leading-6 text-slate-600 dark:text-slate-300">{admin_t('active_users.subtitle')}</p>
                    </div>

                    {activeTab !== 'total' && hasAnyData ? (
                        <div class="flex items-center gap-3 rounded-2xl border border-slate-200 bg-slate-50 px-4 py-3 text-sm text-slate-700 dark:border-slate-700 dark:bg-slate-800/70 dark:text-slate-200">
                            <span class="font-semibold">
                                {activeTab === 'daily'
                                    ? admin_t('active_users.selector.daily')
                                    : (activeTab === 'monthly'
                                        ? admin_t('active_users.selector.monthly')
                                        : admin_t('active_users.selector.yearly'))}
                                :
                            </span>
                            {activeTab === 'daily' ? (
                                <input
                                    type="date"
                                    class="rounded-xl border border-slate-300 bg-white px-3 py-2 text-sm text-slate-900 shadow-sm dark:border-slate-600 dark:bg-slate-900 dark:text-slate-100"
                                    min={pickerMin || undefined}
                                    max={pickerMax || undefined}
                                    value={pickerValue || ''}
                                    onChange={onChangePicker}
                                />
                            ) : (activeTab === 'monthly' ? (
                                <input
                                    type="month"
                                    class="rounded-xl border border-slate-300 bg-white px-3 py-2 text-sm text-slate-900 shadow-sm dark:border-slate-600 dark:bg-slate-900 dark:text-slate-100"
                                    min={pickerMin || undefined}
                                    max={pickerMax || undefined}
                                    value={pickerValue || ''}
                                    onChange={onChangePicker}
                                />
                            ) : (
                                <input
                                    type="number"
                                    class="w-28 rounded-xl border border-slate-300 bg-white px-3 py-2 text-sm text-slate-900 shadow-sm dark:border-slate-600 dark:bg-slate-900 dark:text-slate-100"
                                    min={pickerMin || undefined}
                                    max={pickerMax || undefined}
                                    value={pickerValue || ''}
                                    onChange={onChangePicker}
                                />
                            ))}
                        </div>
                    ) : null}
                </div>

                <div class="mt-6 flex flex-wrap items-center gap-3 border-b border-slate-200 pb-4 dark:border-slate-700">
                    <button type="button" class={tabButtonClass('daily')} id="graph_tab_daily" onClick={() => setActiveTab('daily')}>{admin_t('admin.common.daily')}</button>
                    <button type="button" class={tabButtonClass('monthly')} id="graph_tab_monthly" onClick={() => setActiveTab('monthly')}>{admin_t('admin.common.monthly')}</button>
                    <button type="button" class={tabButtonClass('yearly')} id="graph_tab_yearly" onClick={() => setActiveTab('yearly')}>{admin_t('admin.common.yearly')}</button>
                    <button type="button" class={tabButtonClass('total')} id="graph_tab_total" onClick={() => setActiveTab('total')}>{admin_t('admin.common.total')}</button>
                </div>

                {hasAnyData ? (
                    <div class="mt-6 space-y-6">
                        <div class="grid grid-cols-1 gap-4 xl:grid-cols-3">
                            <Div_kpi_card value={snapshot.visit} title={admin_t('active_users.kpi.visit')} accent="blue">
                                {admin_t('active_users.kpi.top_population')}
                            </Div_kpi_card>
                            <Div_kpi_card value={snapshot.signup} title={admin_t('active_users.kpi.signup')} accent="amber">
                                <div>{admin_t('active_users.kpi.cumulative', { count: au_format_number(snapshot.cumSignup) })}</div>
                                <div>{admin_t('active_users.kpi.cr_visit_signup')}: {au_format_rate(snapshot.cr1)}</div>
                            </Div_kpi_card>
                            <Div_kpi_card value={snapshot.login} title={admin_t('active_users.kpi.login')} accent="emerald">
                                {admin_t('active_users.kpi.cr_signup_login')}: {au_format_rate(snapshot.cr2)}
                            </Div_kpi_card>
                        </div>

                        <div class="space-y-6">
                            <div id="div_funnel_graph" class="h-[360px] w-full rounded-2xl border border-slate-200 bg-white p-3 dark:border-slate-700 dark:bg-slate-900"></div>
                            <div id="div_conv_graph" class="h-[320px] w-full rounded-2xl border border-slate-200 bg-white p-3 dark:border-slate-700 dark:bg-slate-900"></div>
                            <div id="div_trend_graph" class="h-[420px] w-full rounded-2xl border border-slate-200 bg-white p-3 dark:border-slate-700 dark:bg-slate-900"></div>
                        </div>
                    </div>
                ) : (
                    <div class="mt-8 rounded-2xl border border-slate-200 bg-slate-50 px-6 py-12 text-center text-slate-500 dark:border-slate-700 dark:bg-slate-800/60 dark:text-slate-300">
                        {admin_t('active_users.empty')}
                    </div>
                )}
            </div>
        </div>
    );
}

async function render_active_users_dashboard() {
    const mount = document.getElementById('div_content');
    ReactDOM.render(<Div_active_users_loading />, mount);

    try {
        const data = await fetch(admin_build_url('/admin/ajax_get_admin_active_users/')).then(function(res) {
            return res.json();
        });
        ReactDOM.render(<Div_active_users_dashboard data={data} />, mount);
    } catch (error) {
        console.error(error);
        ReactDOM.render(<Div_active_users_error />, mount);
    }
}

function set_main() {
    render_admin_shell('active-users', render_active_users_dashboard);
}
